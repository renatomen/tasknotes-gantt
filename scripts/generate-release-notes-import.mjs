/**
 * Generate `src/releaseNotes.ts` — the in-app "What's New" bundle. Reads the
 * per-version notes under `docs/releases/`, selects the coverage window (the
 * current minor series + the previous minor's patches), sorts newest-first, and
 * emits a TypeScript module that inlines each version's markdown as a STRING
 * LITERAL (this repo builds with Vite/Rollup and has no `.md` loader, unlike
 * TaskNotes' esbuild port).
 *
 * Determinism: dates come from each file's authored `release-date` comment (not
 * git — see scripts/releaseFiles.mjs), and equal-date entries break ties by
 * semver, so the committed file is byte-identical to a CI rebuild on any OS. The
 * file is regenerated at Vite `buildStart` (it is a compile-time input imported
 * by src/main.ts) and is also staged by the `npm version` script; the write is
 * skipped when the content is unchanged so dev rebuilds don't dirty the tree.
 *
 * Runnable directly: `node scripts/generate-release-notes-import.mjs`.
 *
 * @module scripts/generate-release-notes-import
 */
import fs from "node:fs";
import { pathToFileURL } from "node:url";
import { compareVersions, parseVersion, readReleaseEntries } from "./releaseFiles.mjs";

const OUT_PATH = "src/releaseNotes.ts";
const RELEASES_DIR = "docs/releases";
const MANIFEST_PATH = "manifest.json";

/**
 * Select and order the versions to bundle for a given current version: the
 * current minor series plus the previous minor's releases, newest-first with a
 * semver tiebreak for equal dates.
 * @param {{currentVersion:string, entries:ReturnType<typeof readReleaseEntries>}} args
 * @returns {Array<{version:string, content:string, date:string, isCurrent:boolean}>}
 */
export function selectBundle({ currentVersion, entries }) {
  const current = parseVersion(currentVersion);
  if (!current) throw new Error(`Invalid manifest version: ${currentVersion}`);
  const inWindow = entries.filter(
    (e) =>
      e.parsed.major === current.major &&
      (e.parsed.minor === current.minor || e.parsed.minor === current.minor - 1),
  );
  const sorted = [...inWindow].sort((a, b) => {
    if (a.date !== b.date) return a.date < b.date ? 1 : -1; // newest first
    return compareVersions(b.parsed, a.parsed); // stable above its prerelease
  });
  return sorted.map((e) => ({
    version: e.version,
    content: e.content,
    date: e.date,
    isCurrent: e.version === currentVersion,
  }));
}

/**
 * Render the `src/releaseNotes.ts` source. Content is emitted via JSON.stringify
 * so notes prose (which may contain words like "fetch(" or "eval(") becomes inert
 * string data — never a call expression — keeping the bundle hygiene-clean.
 * @param {{currentVersion:string, bundled:ReturnType<typeof selectBundle>}} args
 * @returns {string}
 */
export function renderBundleModule({ currentVersion, bundled }) {
  const items = bundled
    .map(
      (b) =>
        `\t{\n` +
        `\t\tversion: ${JSON.stringify(b.version)},\n` +
        `\t\tcontent: ${JSON.stringify(b.content)},\n` +
        `\t\tdate: ${JSON.stringify(b.date)},\n` +
        `\t\tisCurrent: ${b.isCurrent},\n` +
        `\t}`,
    )
    .join(",\n");
  return (
    `// Auto-generated by scripts/generate-release-notes-import.mjs — do not edit manually.\n` +
    `// Regenerated at build time and staged by \`npm version\`.\n\n` +
    `export interface ReleaseNoteVersion {\n` +
    `\tversion: string;\n` +
    `\tcontent: string;\n` +
    `\tdate: string | null;\n` +
    `\tisCurrent: boolean;\n` +
    `}\n\n` +
    `export const CURRENT_VERSION = ${JSON.stringify(currentVersion)};\n` +
    `export const RELEASE_NOTES_BUNDLE: ReleaseNoteVersion[] = [\n` +
    `${items}${items ? "\n" : ""}` +
    `];\n`
  );
}

/**
 * Regenerate `src/releaseNotes.ts` from the committed notes files. Write-if-
 * different so an unchanged bundle leaves the working tree clean.
 * @param {{releasesDir?:string, manifestPath?:string, outPath?:string, log?:Pick<Console,"log">}} [opts]
 * @returns {"written"|"unchanged"}
 */
export function generate({
  releasesDir = RELEASES_DIR,
  manifestPath = MANIFEST_PATH,
  outPath = OUT_PATH,
  log = console,
} = {}) {
  const currentVersion = JSON.parse(fs.readFileSync(manifestPath, "utf8")).version;
  const entries = readReleaseEntries(releasesDir);
  const bundled = selectBundle({ currentVersion, entries });
  const next = renderBundleModule({ currentVersion, bundled });
  const prev = fs.existsSync(outPath) ? fs.readFileSync(outPath, "utf8") : null;
  if (prev === next) return "unchanged";
  fs.writeFileSync(outPath, next);
  log.log?.(
    `✓ Generated ${outPath} for ${currentVersion} (bundled: ${bundled.map((b) => b.version).join(", ") || "none"})`,
  );
  return "written";
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  generate();
}
